-- 2. CRIAÇÃO DAS TABELAS
CREATE TABLE ZE_SUPPLIERS (
    SUPPLIER_ID INT PRIMARY KEY AUTO_INCREMENT,
    SUPPLIER_NAME VARCHAR(100)
);

CREATE TABLE ZE_PRODUCTS (
    PRODUCT_ID INT PRIMARY KEY AUTO_INCREMENT,
    PRODUCT_NAME VARCHAR(100),
    COST_PRICE DECIMAL(10,2),
    SELLING_PRICE DECIMAL(10,2),
    SUPPLIER_ID INT,
    FOREIGN KEY (SUPPLIER_ID) REFERENCES ZE_SUPPLIERS(SUPPLIER_ID)
);

CREATE TABLE ZE_EMPLOYEES (
    EMPLOYEE_ID INT PRIMARY KEY AUTO_INCREMENT,
    EMPLOYEE_NAME VARCHAR(100),
    SALARY DECIMAL(10,2)
);

CREATE TABLE ZE_CLIENTS (
    CLIENT_ID INT PRIMARY KEY AUTO_INCREMENT,
    CLIENT_NAME VARCHAR(100)
);

CREATE TABLE ZE_SALES (
    SALE_ID INT PRIMARY KEY AUTO_INCREMENT,
    SALE_DATE DATE,
    PRODUCT_ID INT,
    EMPLOYEE_ID INT,
    CLIENT_ID INT,
    QUANTITY INT,
    PAYMENT_STATUS VARCHAR(20), -- 'PAID' OU 'ON_CREDIT'
    FOREIGN KEY (PRODUCT_ID) REFERENCES ZE_PRODUCTS(PRODUCT_ID),
    FOREIGN KEY (EMPLOYEE_ID) REFERENCES ZE_EMPLOYEES(EMPLOYEE_ID),
    FOREIGN KEY (CLIENT_ID) REFERENCES ZE_CLIENTS(CLIENT_ID)
);

