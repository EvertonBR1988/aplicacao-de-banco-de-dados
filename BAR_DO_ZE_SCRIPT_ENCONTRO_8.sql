-- QUESTIONÁRIO --

-- 1. FATURAMENTO MARÇO
SELECT SUM(P.SELLING_PRICE * S.QUANTITY) FROM ZE_SALES S JOIN ZE_PRODUCTS P ON S.PRODUCT_ID = P.PRODUCT_ID 
WHERE MONTH(S.SALE_DATE) = 3;

-- 2. LUCRO MARÇO
SELECT SUM((P.SELLING_PRICE - P.COST_PRICE) * S.QUANTITY) FROM ZE_SALES S 
JOIN ZE_PRODUCTS P ON S.PRODUCT_ID = P.PRODUCT_ID WHERE MONTH(S.SALE_DATE) = 3;

-- 3. LUCRO TRIMESTRE
SELECT SUM((P.SELLING_PRICE - P.COST_PRICE) * S.QUANTITY) FROM ZE_SALES S 
JOIN ZE_PRODUCTS P ON S.PRODUCT_ID = P.PRODUCT_ID;

-- 4. PRODUTO MAIS VENDIDO
SELECT P.PRODUCT_NAME, SUM(S.QUANTITY) FROM ZE_SALES S JOIN ZE_PRODUCTS P 
ON S.PRODUCT_ID = P.PRODUCT_ID GROUP BY P.PRODUCT_NAME ORDER BY SUM(S.QUANTITY) DESC LIMIT 1;

-- 5. PRODUTO MENOS VENDIDO
SELECT P.PRODUCT_NAME, SUM(S.QUANTITY) FROM ZE_SALES S JOIN ZE_PRODUCTS P 
ON S.PRODUCT_ID = P.PRODUCT_ID GROUP BY P.PRODUCT_NAME ORDER BY SUM(S.QUANTITY) ASC LIMIT 1;

-- 6. FUNCIONÁRIO QUE MAIS VENDEU
SELECT E.EMPLOYEE_NAME, SUM(S.QUANTITY) FROM ZE_SALES S JOIN ZE_EMPLOYEES E 
ON S.EMPLOYEE_ID = E.EMPLOYEE_ID GROUP BY E.EMPLOYEE_NAME ORDER BY SUM(S.QUANTITY) DESC LIMIT 1;

-- 7. FUNCIONÁRIO QUE MENOS VENDEU
SELECT E.EMPLOYEE_NAME, SUM(S.QUANTITY) FROM ZE_SALES S JOIN ZE_EMPLOYEES E 
ON S.EMPLOYEE_ID = E.EMPLOYEE_ID GROUP BY E.EMPLOYEE_NAME ORDER BY SUM(S.QUANTITY) ASC LIMIT 1;

-- 8. MAIOR MARGEM UNITÁRIA
SELECT PRODUCT_NAME, (SELLING_PRICE - COST_PRICE) FROM ZE_PRODUCTS 
ORDER BY (SELLING_PRICE - COST_PRICE) DESC LIMIT 1;

-- 9. MENOR MARGEM UNITÁRIA
SELECT PRODUCT_NAME, (SELLING_PRICE - COST_PRICE) FROM ZE_PRODUCTS 
ORDER BY (SELLING_PRICE - COST_PRICE) ASC LIMIT 1;

-- 10. VENDAS POR DIA
SELECT SALE_DATE, COUNT(*) FROM ZE_SALES GROUP BY SALE_DATE;

-- 11. CLIENTE QUE MAIS COMPROU
SELECT C.CLIENT_NAME, SUM(S.QUANTITY) FROM ZE_SALES S JOIN ZE_CLIENTS C ON S.CLIENT_ID = C.CLIENT_ID 
GROUP BY C.CLIENT_NAME ORDER BY SUM(S.QUANTITY) DESC LIMIT 1;

-- 12. VALOR INVESTIDO EM ESTOQUE
SELECT SUM(COST_PRICE * 50) FROM ZE_PRODUCTS;

-- 13. FORNECEDOR COM MAIOR CUSTO MÉDIO
SELECT SU.SUPPLIER_NAME, AVG(P.COST_PRICE) FROM ZE_PRODUCTS P JOIN ZE_SUPPLIERS SU 
ON P.SUPPLIER_ID = SU.SUPPLIER_ID GROUP BY SU.SUPPLIER_NAME ORDER BY AVG(P.COST_PRICE) DESC LIMIT 1;

-- 14. QUEM MAIS DEVE
SELECT C.CLIENT_NAME, SUM(S.QUANTITY) FROM ZE_SALES S JOIN ZE_CLIENTS C ON S.CLIENT_ID = C.CLIENT_ID 
WHERE S.PAYMENT_STATUS = 'ON_CREDIT' GROUP BY C.CLIENT_NAME ORDER BY SUM(S.QUANTITY) DESC LIMIT 1;

-- 15. PRODUTO MAIS LEVADO NO FIADO
SELECT P.PRODUCT_NAME, SUM(S.QUANTITY) FROM ZE_SALES S JOIN ZE_PRODUCTS P ON S.PRODUCT_ID = P.PRODUCT_ID 
WHERE S.PAYMENT_STATUS = 'ON_CREDIT' GROUP BY P.PRODUCT_NAME ORDER BY SUM(S.QUANTITY) DESC LIMIT 1;

-- 16. PERDA POR MÊS
SELECT MONTH(SALE_DATE), SUM(P.SELLING_PRICE * S.QUANTITY) FROM ZE_SALES S JOIN ZE_PRODUCTS P 
ON S.PRODUCT_ID = P.PRODUCT_ID WHERE S.PAYMENT_STATUS = 'ON_CREDIT' GROUP BY MONTH(SALE_DATE);

